SELECT COUNTED.[AssetID]
      ,COUNTED.[Code]
      ,COUNTED.[Name]
      ,COUNTED.[BranchID]
      ,COUNTED.[Status]
      ,COUNTED.[Date]
      ,COUNTED.[UserID]
      ,COUNTED.[UserBranch]
      ,COUNTED.[RoundID]
      ,MAIN.[imagePath]
      ,MAIN.[imagePath_2]
      ,COUNTED.[detail]
FROM [TEST_OPS].[dbo].[Fix_Assets_Counted] COUNTED
LEFT JOIN [TEST_OPS].[dbo].[Assets] MAIN ON MAIN.[Code] = COUNTED.[Code]
WHERE [Code]=@Code AND COUNTED.[Status]=0 AND COUNTED.[RoundID]=@RoundID
